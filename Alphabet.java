//printing sequence of alphabets without looping
import java.util.Scanner;
class Alphabet
{
public static void main(String args[])
{
char alpha;
Scanner sc=new Scanner(System.in);
alpha=sc.next().charAt(0);
alpha++;
System.out.println(alpha);
alpha++;
System.out.println(alpha);
alpha++;
System.out.println(alpha);
alpha++;
System.out.println(alpha);
alpha++;
System.out.println(alpha);
}}